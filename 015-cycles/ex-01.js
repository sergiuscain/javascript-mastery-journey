// Цикл while используется для вывода чисел от 0 до 4
let j = 0;
while (j < 5) {
    console.log(j);
    j++;
}

// Цикл for используется для вывода чисел от 0 до 4
for (let i = 0; i < 5; i++) {
    console.log(i); // Выведет числа от 0 до 4
}

// Цикл do...while используется для вывода чисел от 0 до 4
let k = 0;
do {
    console.log(k);
    k++;
} while (k < 5);

